# A = [[1, 2], [3, 4]]
# row 0 = [1, 2]
# row 1 = [3, 4]
#
# column 0 = [1, 3]
# column 1 = [2, 4]
#
# Part 2
# A.T = [[1, 3], [2, 4]]
# row 0 = [1, 3] == column 0
# row 1 = [2, 4] == column 1
#
# Part 3
# формула (A@B)[i,j] =  рядок i першої матриці · стовпець j другої матриці. У нас A.T @ A, тобто перша матриця — A.T, друга — A:
#
# A.T   = [[1, 3],
#          [2, 4]]
# A     = [[1, 2],
#          [3, 4]]
#
# [(A.T row0 * A col0), (A.T row0 * A col1), (A.T row1 * A col0), (A.T row1 * A col1)]
#
# result = [row0 * col0, row0 * col1,
#           row1 * col0, row1 * col1]
#
# [0,0] = A.T ([1, 3]) * A ([1, 3]) = 10
# [0,1] = A.T ([1, 3]) * A ([2, 4]) = 14
# [1,0] = A.T ([2, 4]) * A ([1, 3]) = 14
# [1,1] = A.T ([2, 4]) * A ([2, 4]) = 20
#
# result = [10, 14, 14, 20]
#
#
#
