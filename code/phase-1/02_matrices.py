# Задачі 3: top_k_similar(X, q, k=5) — індекси топ-5 найсхожіших векторів з X до q, без циклу.
# Подивись на np.argsort (сортує масив і повертає індекси, а не значення) — подумай як з цього дістати "топ-5 найбільших".

def top_k_similar(X, q, k=5):
    scores = (X @ q / (np.linalg.norm(X, axis=1) * np.linalg.norm(q)))
    top_k = np.argsort(scores)[-k:]
    return top_k

np.random.seed(0)
X = np.random.rand(100_000, 1536)
q = np.random.rand(1536)

print(top_k_similar(X, q, k=5))

# =============================================================
# Схема: 3 рівні "множення" -- від вектора до матриці
# =============================================================
#
# 1) ВЕКТОР x ВЕКТОР (dot product) -> ЧИСЛО
#
#    a = [1  2  3]
#    b = [4  5  6]
#
#    a @ b = 1*4 + 2*5 + 3*6 = 32        <- просто ЧИСЛО
#
#
# 2) ТАБЛИЦЯ (матриця) x ВЕКТОР -> ВЕКТОР
#
#    A = [1  2]     v = [5]
#        [3  4]         [6]
#
#    рядок0 . v = 1*5 + 2*6 = 17   -\
#    рядок1 . v = 3*5 + 4*6 = 39   -/  -> [17]
#                                        [39]
#
#    A @ v = [17, 39]        <- ВЕКТОР (по одному числу на рядок A)
#
#
# 3) ТАБЛИЦЯ (матриця) x ТАБЛИЦЯ (матриця) -> ТАБЛИЦЯ (матриця)
#
#    A = [1  2]     B = [5  6]
#        [3  4]         [7  8]
#
#               стовпець0   стовпець1
#                 (5,7)       (6,8)
#    рядок0 (1,2) | 1*5+2*7=19 | 1*6+2*8=22 |
#    рядок1 (3,4) | 3*5+4*7=43 | 3*6+4*8=50 |
#
#    A @ B = [19  22]        <- ТАБЛИЦЯ (кожна клітинка = свій dot product рядка і стовпця)
#            [43  50]
#
# Патерн: чим більше "рядків"/"стовпців" залучено -- тим складніша форма результату:
# 1 вектор + 1 вектор = число; таблиця + 1 вектор = вектор; таблиця + таблиця = таблиця.


