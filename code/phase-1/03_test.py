import numpy as np

A = np.array([[4, 0], [0, 5]])

print(A)

print(A @ [1, 0])
print(A @ [0, 1])
print(A @ [1, 1])

# [[4 0]
#  [0 5]]
#
# [1, 0] = [4, 0] * [0, 0] = [4, 0]
# [0, 1] = [0, 0] * [0, 5] = [0, 5]
# [1, 1] = [4, 0] * [0, 5] + [0, 5] * [0, 0] = [4, 0] + [0, 5] = [4, 5]
#
#
#
#
#
# [0, 0] = 0 + 0 = 0
# [1, 0] = 4 + 0 = 4
# [0, 1] = 0 + 5 = 5
# [1, 1] = 4 + 5 = 9
